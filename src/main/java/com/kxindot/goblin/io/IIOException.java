package com.kxindot.goblin.io;

/**
 * @author ZhaoQingJiang
 */
public class IIOException extends RuntimeException {

	private static final long serialVersionUID = 4198265608056316377L;

	public IIOException() {
		super();
	}

	public IIOException(String message, Throwable cause) {
		super(message, cause);
	}

	public IIOException(String message) {
		super(message);
	}

	public IIOException(Throwable cause) {
		super(cause);
	}
	
}
