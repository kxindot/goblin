package com.kxindot.goblin;

public interface Symbol {

    static final String Empty = "";
    static final String WhiteSpace = " ";
    static final String Tab = "    ";
    static final String LineFeed = "\n";
    
    static final String Percent = "%";
    static final String Asterisk = "*";
    static final String Hyphen = "-";
    static final String Cross = "+";
    static final String LP = "(";
    static final String RP = ")";
    static final String LCB = "{";
    static final String RCB = "}";
    static final String LSB = "[";
    static final String RSB = "]";
    static final String LAB = "<";
    static final String RAB = ">";
    static final String EA = "=";
    static final String Semicolon = ";";
    static final String Colon = ":";
    static final String Comma = ",";
    static final String Dot = ".";
    
    static final String QM = "?";
    
    /** 感叹号(!) - exclamation marker*/
    String EM = "!";
    static final String Slash = "/";
    static final String BackSlash = "\\";
    static final String VL = "|";
    
}
